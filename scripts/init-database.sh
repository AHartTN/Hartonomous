#!/bin/bash
# ============================================================================
# Hartonomous Database Initialization Script
# 
# Initializes PostgreSQL database with all required schema, functions, and
# indexes for Hartonomous system.
#
# Usage:
#   ./scripts/init-database.sh [environment]
#
# Environments:
#   - localhost (default)
#   - development
#   - staging
#   - production
#
# Copyright (c) 2025 Anthony Hart. All Rights Reserved.
# ============================================================================

set -e  # Exit on error
set -u  # Exit on undefined variable
set -o pipefail  # Exit on pipe failure

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# Environment (default: localhost)
ENVIRONMENT="${1:-localhost}"

echo -e "${BLUE}???????????????????????????????????????????????????????????${NC}"
echo -e "${BLUE}  Hartonomous Database Initialization${NC}"
echo -e "${BLUE}  Environment: ${ENVIRONMENT}${NC}"
echo -e "${BLUE}???????????????????????????????????????????????????????????${NC}"

# ============================================================================
# Configuration
# ============================================================================

# Get script directory
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"
SCHEMA_DIR="${PROJECT_ROOT}/schema"

# Load environment variables
if [ -f "${PROJECT_ROOT}/.env" ]; then
    echo -e "${GREEN}?${NC} Loading .env file"
    export $(grep -v '^#' "${PROJECT_ROOT}/.env" | xargs)
fi

# Database connection
PGHOST="${PGHOST:-localhost}"
PGPORT="${PGPORT:-5432}"
PGUSER="${PGUSER:-hartonomous}"
PGDATABASE="${PGDATABASE:-hartonomous}"

# Validate PostgreSQL connection
echo -e "${YELLOW}?${NC}  Testing database connection..."
if ! PGPASSWORD="${PGPASSWORD}" psql -h "${PGHOST}" -p "${PGPORT}" -U "${PGUSER}" -d "${PGDATABASE}" -c "SELECT 1" > /dev/null 2>&1; then
    echo -e "${RED}?${NC} Failed to connect to PostgreSQL"
    echo -e "   Host: ${PGHOST}:${PGPORT}"
    echo -e "   User: ${PGUSER}"
    echo -e "   Database: ${PGDATABASE}"
    exit 1
fi
echo -e "${GREEN}?${NC} Database connection successful"

# ============================================================================
# Helper Functions
# ============================================================================

execute_sql_file() {
    local file="$1"
    local description="$2"
    
    echo -e "${BLUE}?${NC} ${description}"
    
    if [ ! -f "${file}" ]; then
        echo -e "${RED}?${NC} File not found: ${file}"
        return 1
    fi
    
    if PGPASSWORD="${PGPASSWORD}" psql -h "${PGHOST}" -p "${PGPORT}" -U "${PGUSER}" -d "${PGDATABASE}" -f "${file}" > /dev/null 2>&1; then
        echo -e "${GREEN}?${NC} Success"
        return 0
    else
        echo -e "${RED}?${NC} Failed to execute: ${file}"
        return 1
    fi
}

execute_sql_pattern() {
    local pattern="$1"
    local description="$2"
    
    echo -e "\n${YELLOW}???${NC} ${description} ${YELLOW}???${NC}"
    
    local count=0
    for file in ${pattern}; do
        if [ -f "${file}" ]; then
            local filename=$(basename "${file}")
            execute_sql_file "${file}" "  ${filename}"
            ((count++)) || true
        fi
    done
    
    if [ ${count} -eq 0 ]; then
        echo -e "${YELLOW}?${NC}  No files found matching pattern"
    fi
    
    echo -e "${GREEN}?${NC} Completed ${count} files"
}

# ============================================================================
# Schema Initialization
# ============================================================================

echo -e "\n${BLUE}???????????????????????????????????????????????????????????${NC}"
echo -e "${BLUE}  Step 1: Extensions${NC}"
echo -e "${BLUE}???????????????????????????????????????????????????????????${NC}"

# Extensions (in order)
execute_sql_file "${SCHEMA_DIR}/extensions/001_postgis.sql" "PostGIS (spatial types)"
execute_sql_file "${SCHEMA_DIR}/extensions/003_pg_trgm.sql" "pg_trgm (trigram similarity)"
execute_sql_file "${SCHEMA_DIR}/extensions/004_btree_gin.sql" "btree_gin (GIN indexes)"
execute_sql_file "${SCHEMA_DIR}/extensions/005_pgcrypto.sql" "pgcrypto (SHA-256 hashing)"

# PL/Python (optional - skip if not available)
if PGPASSWORD="${PGPASSWORD}" psql -h "${PGHOST}" -p "${PGPORT}" -U "${PGUSER}" -d "${PGDATABASE}" -c "CREATE EXTENSION IF NOT EXISTS plpython3u" > /dev/null 2>&1; then
    echo -e "${GREEN}?${NC} PL/Python3u extension enabled"
else
    echo -e "${YELLOW}?${NC}  PL/Python3u not available (optional)"
fi

# Apache AGE (EXPERIMENTAL - skip by default)
if [ "${AGE_ENABLED:-false}" = "true" ]; then
    execute_sql_file "${SCHEMA_DIR}/extensions/006_age.sql" "Apache AGE (EXPERIMENTAL)" || echo -e "${YELLOW}?${NC}  AGE not available (optional)"
fi

echo -e "\n${BLUE}???????????????????????????????????????????????????????????${NC}"
echo -e "${BLUE}  Step 2: Custom Types${NC}"
echo -e "${BLUE}???????????????????????????????????????????????????????????${NC}"

execute_sql_file "${SCHEMA_DIR}/types/001_modality_type.sql" "modality_type enum"
execute_sql_file "${SCHEMA_DIR}/types/002_relation_type.sql" "relation_type enum"

echo -e "\n${BLUE}???????????????????????????????????????????????????????????${NC}"
echo -e "${BLUE}  Step 3: Core Tables${NC}"
echo -e "${BLUE}???????????????????????????????????????????????????????????${NC}"

execute_sql_file "${SCHEMA_DIR}/core/tables/001_atom.sql" "atom table"
# Note: 002_atom_composition.sql is deprecated - compositions now stored in atom.composition_ids
execute_sql_file "${SCHEMA_DIR}/core/tables/003_atom_relation.sql" "atom_relation table"
execute_sql_file "${SCHEMA_DIR}/core/tables/004_history_tables.sql" "history tables"
execute_sql_file "${SCHEMA_DIR}/core/tables/005_ooda_tables.sql" "OODA loop tables"

echo -e "\n${BLUE}???????????????????????????????????????????????????????????${NC}"
echo -e "${BLUE}  Step 4: Indexes${NC}"
echo -e "${BLUE}???????????????????????????????????????????????????????????${NC}"

execute_sql_pattern "${SCHEMA_DIR}/core/indexes/core/*.sql" "Core Indexes"
execute_sql_pattern "${SCHEMA_DIR}/core/indexes/spatial/*.sql" "Spatial Indexes"
execute_sql_pattern "${SCHEMA_DIR}/core/indexes/relations/*.sql" "Relation Indexes"
execute_sql_pattern "${SCHEMA_DIR}/core/indexes/composition/*.sql" "Composition Indexes"

echo -e "\n${BLUE}???????????????????????????????????????????????????????????${NC}"
echo -e "${BLUE}  Step 5: Functions${NC}"
echo -e "${BLUE}???????????????????????????????????????????????????????????${NC}"

execute_sql_pattern "${SCHEMA_DIR}/core/functions/atomization/*.sql" "Atomization Functions"
execute_sql_pattern "${SCHEMA_DIR}/core/functions/spatial/*.sql" "Spatial Functions"
execute_sql_pattern "${SCHEMA_DIR}/core/functions/relations/*.sql" "Relation Functions"
execute_sql_pattern "${SCHEMA_DIR}/core/functions/composition/*.sql" "Composition Functions"
execute_sql_pattern "${SCHEMA_DIR}/core/functions/ooda/*.sql" "OODA Loop Functions"
execute_sql_pattern "${SCHEMA_DIR}/core/functions/provenance/*.sql" "Provenance Functions"

echo -e "\n${BLUE}???????????????????????????????????????????????????????????${NC}"
echo -e "${BLUE}  Step 6: Triggers${NC}"
echo -e "${BLUE}???????????????????????????????????????????????????????????${NC}"

execute_sql_file "${SCHEMA_DIR}/core/triggers/001_temporal_versioning.sql" "Temporal versioning triggers"
execute_sql_file "${SCHEMA_DIR}/core/triggers/002_reference_counting.sql" "Reference counting triggers"
execute_sql_file "${SCHEMA_DIR}/core/triggers/003_provenance_notify.sql" "Provenance notification triggers"

echo -e "\n${BLUE}???????????????????????????????????????????????????????????${NC}"
echo -e "${BLUE}  Step 7: Views${NC}"
echo -e "${BLUE}???????????????????????????????????????????????????????????${NC}"

execute_sql_pattern "${SCHEMA_DIR}/views/*.sql" "Analytical Views"

# ============================================================================
# Validation
# ============================================================================

echo -e "\n${BLUE}???????????????????????????????????????????????????????????${NC}"
echo -e "${BLUE}  Validation${NC}"
echo -e "${BLUE}???????????????????????????????????????????????????????????${NC}"

# Count tables
TABLE_COUNT=$(PGPASSWORD="${PGPASSWORD}" psql -h "${PGHOST}" -p "${PGPORT}" -U "${PGUSER}" -d "${PGDATABASE}" -t -c "
    SELECT COUNT(*) FROM information_schema.tables 
    WHERE table_schema = 'public' 
    AND table_type = 'BASE TABLE'
")

# Count functions
FUNCTION_COUNT=$(PGPASSWORD="${PGPASSWORD}" psql -h "${PGHOST}" -p "${PGPORT}" -U "${PGUSER}" -d "${PGDATABASE}" -t -c "
    SELECT COUNT(*) FROM pg_proc 
    WHERE pronamespace = (SELECT oid FROM pg_namespace WHERE nspname = 'public')
")

# Count indexes
INDEX_COUNT=$(PGPASSWORD="${PGPASSWORD}" psql -h "${PGHOST}" -p "${PGPORT}" -U "${PGUSER}" -d "${PGDATABASE}" -t -c "
    SELECT COUNT(*) FROM pg_indexes 
    WHERE schemaname = 'public'
")

echo -e "${GREEN}?${NC} Tables:    ${TABLE_COUNT}"
echo -e "${GREEN}?${NC} Functions: ${FUNCTION_COUNT}"
echo -e "${GREEN}?${NC} Indexes:   ${INDEX_COUNT}"

# Test basic atomization
echo -e "\n${YELLOW}?${NC} Testing atomization..."
TEST_RESULT=$(PGPASSWORD="${PGPASSWORD}" psql -h "${PGHOST}" -p "${PGPORT}" -U "${PGUSER}" -d "${PGDATABASE}" -t -c "
    SELECT atomize_value('\\x48'::bytea, 'H', '{\"modality\": \"character\"}'::jsonb);
" 2>&1)

if [ $? -eq 0 ]; then
    echo -e "${GREEN}?${NC} Atomization test successful (atom_id: ${TEST_RESULT})"
else
    echo -e "${RED}?${NC} Atomization test failed"
    echo -e "   ${TEST_RESULT}"
fi

# ============================================================================
# Summary
# ============================================================================

echo -e "\n${BLUE}???????????????????????????????????????????????????????????${NC}"
echo -e "${GREEN}?  Database initialization complete!${NC}"
echo -e "${BLUE}???????????????????????????????????????????????????????????${NC}"
echo -e ""
echo -e "  Database:  ${PGDATABASE}"
echo -e "  Host:      ${PGHOST}:${PGPORT}"
echo -e "  User:      ${PGUSER}"
echo -e ""
echo -e "${YELLOW}Next steps:${NC}"
echo -e "  1. Start the API:     ${BLUE}uvicorn api.main:app --reload${NC}"
echo -e "  2. View docs:         ${BLUE}http://localhost:8000/docs${NC}"
echo -e "  3. Health check:      ${BLUE}curl http://localhost:8000/v1/health${NC}"
echo -e ""
echo -e "${BLUE}???????????????????????????????????????????????????????????${NC}"
